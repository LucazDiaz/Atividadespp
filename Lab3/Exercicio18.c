#include <stdio.h>

int main() {
  int ano, ano;
  float salario = 2000, aumento = 0.015;

  printf("Informe o ano atual: ");
  scanf("%d", &ano);

  for (ano = 1996; ano <= ano; ano++) {
    salario += salario * aumento;
    aumento *= 2;
  }

  printf("O salario atual do funcionario eh R$%.2f\n", salario);

  return 0;
}
